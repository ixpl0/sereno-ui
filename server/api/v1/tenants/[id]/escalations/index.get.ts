import { getMockEscalations, isValidToken } from '../../../../../utils/mockData'
import { toEscalationResponse } from '../../../../../utils/escalationResponse'

export default defineEventHandler((event) => {
  const token = getCookie(event, 'auth_token')

  if (!isValidToken(token)) {
    throw createError({
      statusCode: 401,
      statusMessage: 'Unauthorized',
      data: { error: { code: 'unauthorized', message: 'Authentication required' } },
    })
  }

  const tenantId = getRouterParam(event, 'id')
  if (!tenantId) {
    throw createError({
      statusCode: 400,
      statusMessage: 'Bad Request',
      data: { error: { code: 'invalid_request', message: 'tenant id is required' } },
    })
  }

  const escalations = getMockEscalations(tenantId).map(toEscalationResponse)

  return { escalations }
})
